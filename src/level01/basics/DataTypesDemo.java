package level01.basics;

public class DataTypesDemo {
    public static void main(String[] args) {

        String companyName = "Infosys";
        byte experience = 1;
        short passoutYear = 2026;
        int salary = 55000;
        long mobileNumber = 9910360596L;
        float percentage = 69.5f;
        double cgpa = 6.90;
        char gender = 'M';
        boolean isPlaced = true;


        System.out.println("Company Name : " + companyName);
        System.out.println("Experience   : " + experience);
        System.out.println("Passout Year : " + passoutYear);
        System.out.println("Salary       : " + salary);
        System.out.println("Mobile No.   : " + mobileNumber);
        System.out.println("Percentage   : " + percentage);
        System.out.println("CGPA         : " + cgpa);
        System.out.println("Gender       : " + gender);
        System.out.println("Placed       : " + isPlaced);
    }
}
